/**
 * @brief Helper function to read a value from a register
 * 
 * Read a value from a register of BMI270. Notice that BMI270 SDO data has additional dummy byte
 * 
 * @param ctx Pointer to a BMI270 sensor context
 * @param reg Starting register address
 * @param data Buffer array where data will be saved to
 * @param size Number of bytes to read (exclude dummy bytes)
 * 
 * @return 0 if successfully, error code otherwise
 */
static int bmi270_reg_read(BMI270_Context *ctx, uint8_t reg, uint8_t *data, uint8_t len) {
    int err;

    /* Set the register address with the read bit */
    uint8_t tx_buffer = reg | 0x80;   // Set MSB to indicate a read operation
    uint8_t rx_dummy[2];              // Dummy byte buffer (For BMI270, this is two, check other sensors)

    /* Set up transmit buffer for register address */
    struct spi_buf tx_spi_buf = {
        .buf = &tx_buffer,
        .len = 1
    };
    struct spi_buf_set tx_spi_buf_set = {
        .buffers = &tx_spi_buf,
        .count = 1
    };

    /* Set up receive buffers */
    struct spi_buf rx_spi_bufs[2] = {
        {.buf = &rx_dummy, .len = 2},   // Dummy byte buffer
        {.buf = data, .len = len}      // Actual data buffer
    };
    struct spi_buf_set rx_spi_buf_set = {
        .buffers = rx_spi_bufs,
        .count = ARRAY_SIZE(rx_spi_bufs)
    };

    /* Perform the SPI transaction */
    err = spi_transceive_dt(ctx->spec, &tx_spi_buf_set, &rx_spi_buf_set);
    if (err < 0) {
        LOG_ERR("spi_transceive_dt() failed, err: %d", err);
        return err;
    }

    /* Add a short delay for device stabilization */
    k_usleep(2);

    return 0;
}

/**
 * @brief Helper function to write a value to registers
 * 
 * Write values to registers of BMI270
 * 
 * @param ctx Pointer to a BMI270 sensor context
 * @param start Start address of the register to read
 * @param data Buffer array to store the value to be written
 * @param len Number of bytes to write
 * 
 * @return 0 if successful, error code otherwise
 */
static int bmi270_reg_write(BMI270_Context *ctx, uint8_t start, const uint8_t *data, uint16_t len){
	int ret;

    /* Set the bit to write command - bit7 is 0 */
	uint8_t addr = start & 0x7F;

    /* Prepare the write buffer, with first element being the command, second element being the value */
	const struct spi_buf tx_buf[2] = {
		{.buf = &addr, .len = sizeof(addr)},
		{.buf = (uint8_t *)data, .len = len}
	};
	const struct spi_buf_set tx = {
		.buffers = tx_buf,
		.count = ARRAY_SIZE(tx_buf)
	};

	ret = spi_write_dt(ctx->spec, &tx);
	if (ret < 0) {
		LOG_ERR("spi_write_dt failed %i", ret);
		return ret;
	}

	k_usleep(2);
    
	return 0;
}

/**
 * @brief Write to a register with delay afterwards
 * 
 * Share the same input/output with bmi270_reg_write
 */
static int bmi270_reg_write_with_delay(BMI270_Context *ctx, uint8_t start, const uint8_t *data, uint16_t len, uint32_t delay_us){
    int ret = 0;

    ret = bmi270_reg_write(ctx, start, data, len);
    if (ret == 0){
        k_usleep(delay_us);
    }
    return ret; 
}

