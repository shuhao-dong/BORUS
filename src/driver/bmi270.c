/**
 * @brief Helper function to set FIFO watermark level
 * 
 * Configure FIFO water mark level. Max value is 8191
 * 
 * @param ctx Pointer to a BMI270 sensor context
 * @param watermark Watermark level
 * 
 * @return 0 if successful
 */
static int bmi270_conf_fifo_watermark(BMI270_Context *ctx, uint16_t watermark){
    int ret = 0;
    uint8_t tx_bufs[2];

    if (watermark > (0x1F * 256 + 0xFF)){
        LOG_ERR("FIFO watermark level overflow: maximum allowed is 8191");
        return -1; 
    }

    tx_bufs[0] = watermark & 0xFF;
    tx_bufs[1] = (watermark >> 8) & 0x1F;

    ret = bmi270_reg_write(ctx, BMI270_REG_FIFO_WTM_0, tx_bufs, sizeof(tx_bufs)); 
    if (ret != 0){
        LOG_ERR("Failed to configure FIFO watermark level");
        return ret;
    }
    return 0; 
}

/**
 * @brief Helper function to enable/disable sensor components
 * 
 * Choose to enable/disable acc, gyr, temp and aux for BMI270
 * 
 * @param ctx A pointer to a BMI270 sensor context
 * @param acc_enable Flag to enable accelerometer
 * @param gyr_enable Flag to enable gyroscope
 * @param aux_enable Flag to enable auxiliary sensor 
 * @param temp_enable Flag o enble die temperature sensor
 * 
 * @return 0 if successful, err code otherwise
 */
static int bmi270_enable_sensor(BMI270_Context *ctx, 
        bool acc_enable, bool gyr_enable, bool aux_enable, bool temp_enable)
{
    int ret = 0;
    uint8_t sensor_enable = (aux_enable | 
                        (gyr_enable << 1) |
                        (acc_enable << 2) |
                        (temp_enable << 3)); 
    ret = bmi270_reg_write(ctx, BMI270_REG_PWR_CTRL, &sensor_enable, sizeof(sensor_enable)); 
    if (ret != 0){
        LOG_ERR("Failed to enable sensors");
        return ret; 
    }
    return 0; 
}

/**
 * @brief Helper function to configure power profile for the sensor
 * 
 * @param ctx A pointer to a BMI270 sensor context
 * @param config Power configuration file
 * 
 * @retval 0 if successful, err otherwise  
 */
static int bmi270_conf_pwr(BMI270_Context *ctx, bmi270_pwr_config_t *config){
    int ret = 0;
    
    uint8_t acc_conf, gyr_conf;
    acc_conf = ((config->high_perf_en) << 7);
    gyr_conf = ((config->gyr_filter_perf) << 7) | ((config->gyr_noise_perf) << 6);

    ret = bmi270_reg_write(ctx, BMI270_REG_ACC_CONF, &acc_conf, sizeof(acc_conf));
    if (ret != 0){
        LOG_ERR("Failed to configure acc power mode");
        return ret;
    }

    ret = bmi270_reg_write(ctx, BMI270_REG_GYR_CONF, &gyr_conf, sizeof(gyr_conf));
    if (ret != 0){
        LOG_ERR("Failed to configure gyr power mode");
        return ret;
    }

    return 0; 
}

/**
 * @brief Convert raw acceleration value to m^2/s
 * 
 * @param val An array to save the converted values
 * @param raw_value Raw acc values obtained from the register
 * @param range Configured acceleration measurement range
 * 
 */
static void bmi270_acc_convert_raw(struct sensor_value *val, int64_t raw_value, uint8_t range){
    /* Convert from raw value to m^2/s */
    raw_value = (raw_value * SENSOR_G * (int64_t)range) / INT16_MAX;
    val->val1 = raw_value / 1000000LL;
    val->val2 = raw_value % 1000000LL;
}

/**
 * @brief Convert raw gyroscope value to rad/s
 * 
 * @param val An array to save the converted values
 * @param raw_value Raw gyr values obtained from the register
 * @param range Configured gyroscope measurement range 
 */
static void bmi270_gyr_convert_raw(struct sensor_value *val, int64_t raw_value, uint16_t range){
    /* Convert from degrees/s to rad/s */
    raw_value = (raw_value * SENSOR_PI * (int64_t)range) / (180LL * INT16_MAX); 
    val->val1 = raw_value / 1000000LL;
    val->val2 = raw_value % 1000000LL; 
}

