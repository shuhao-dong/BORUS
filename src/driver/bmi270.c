/**
 * @brief Configure interrupt electrical behaviour and type 
 * 
 * @param ctx A pointer to a BMI270 sensor context
 * @param config An interrupt configuration (electrical behaviour)
 * @param pin A number between 1 and 2
 * 
 * @retval 0 if successful, err otherwise
 */
int bmi270_conf_interrupt(BMI270_Context *ctx, bmi270_interrupt_config_t *config, uint8_t pin){
    int ret = 0;
    uint8_t int_type_data; 
    uint8_t target_reg; 

    /* Configurations for electrical behaviour */
    uint8_t elec_int_config = (config->input_en << 4) |
                        (config->output_en << 3) |
                        (config->odb << 2) |
                        (config->lvl << 1);

    /* Configuration for latch mode */
    uint8_t int_latch = config->int_latch;
    ret = bmi270_reg_write(ctx, BMI270_REG_INT_LATCH, &int_latch, 1);
    if (ret != 0){
        LOG_ERR("Failed to configure interrupt latch mode");
        return ret;
    }
    LOG_DBG("Latch mode configured");

    /* Feature interrupt or data interrupt */
    bool type_int = config->use_data;
    if (type_int == 0){
        // If use feature interrupt: configuration register is different for INT1 and INT2, configuration data is the same
        int_type_data = config->interrupt_type.feature_int;
        if (pin == 1){
            target_reg = BMI270_REG_INT1_MAP_FEAT;
        }else if (pin == 2){
            target_reg = BMI270_REG_INT2_MAP_FEAT;
        }else{
            LOG_ERR("Invalid INT pin selection");
            return -1; 
        }
    }else if (type_int == 1){
        // If use data interrupt: configuration register is the same for INT1 and INT2, configuration data is different
        target_reg = BMI270_REG_INT_MAP_DATA; 
        int_type_data = config->interrupt_type.data_int; 
        if (pin == 1){
            int_type_data = int_type_data; 
        }else if (pin == 2){
            int_type_data = int_type_data << 4; 
        }else{
            LOG_ERR("Invalid INT pin selection");
            return -1; 
        }
    }else{
        LOG_ERR("Invalid INT type: choose between 0 (feature) and 1 (data)");
        return -1; 
    }
    
    switch (pin){
    case 1:
        ret = bmi270_reg_write(ctx, BMI270_REG_INT1_IO_CTRL, &elec_int_config, 1);
        if (ret != 0){
            LOG_ERR("Failed to configure interrupt %d's behaviour", pin);
            return ret; 
        }
        ret = bmi270_reg_write(ctx, target_reg, &int_type_data, 1); 
        if (ret != 0){
            LOG_ERR("Failed to configure interrupt %d @0x%02X", pin, target_reg);
            return ret; 
        }
        LOG_DBG("Interrupt 1 configured"); 
        break;
    case 2:
        ret = bmi270_reg_write(ctx, BMI270_REG_INT2_IO_CTRL, &elec_int_config, 1); 
        if (ret != 0){
            LOG_ERR("Failed to configure interrupt %d's behaviour", pin);
            return ret; 
        }
        ret = bmi270_reg_write(ctx, target_reg, &int_type_data, 1);
        if (ret != 0){
            LOG_ERR("Failed to configure interrupt %d @0x%02X", pin, target_reg);
            return ret; 
        }
        LOG_DBG("Interrupt 2 configured"); 
        break;
    default:
        LOG_ERR("Select interrupt 1 or interrupt 2"); 
        break;
    }
    return 0; 
}

uint16_t bmi270_read_int_status(BMI270_Context *ctx){
    int ret = 0;
    uint8_t int_status[2];

    ret = bmi270_reg_read(ctx, BMI270_REG_INT_STATUS_0, int_status, sizeof(int_status));
    if (ret != 0){
        LOG_ERR("Failed to read interrupt status");
    }
    LOG_INF("The interrupt status is: 0x%02X\t0x%02X", int_status[0], int_status[1]); 

    uint16_t ret_status = (int_status[0] << 8) | (int_status[1]); 
    
    return ret_status;
}

uint8_t bmi270_read_fifo_length(BMI270_Context *ctx){
    int ret = 0;

    uint8_t buf[2];
    ret = bmi270_reg_read(ctx, BMI270_REG_FIFO_LENGTH_0, buf, sizeof(buf));
    if (ret != 0){
        LOG_ERR("Failed to read FIFO length");
    }

    uint8_t fifo_length;
    fifo_length = (buf[1] << 8) | buf[0];

    return fifo_length; 
}

