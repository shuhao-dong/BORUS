/**
 * @brief Set BMI270 to operation mode
 * 
 * Set BMI270 to normal, low-power or suspend mode, choose sensors to be enabled
 * 
 * @note In suspend mode, all sensors are turned off. In low-power mode, 
 * 
 * @param ctx A pointer to a BMI270 sensor context
 * @param mode Operation mode 
 * @param acc_enable Enable accelerometer
 * @param gyr_enable Enable gyroscope
 * 
 * @return 0 if successful
 */
void bmi270_set_mode(BMI270_Context *ctx, BMI270_PowerMode mode, bool acc_enable, bool gyr_enable){
    /* Enable sensors */
    if (acc_enable && gyr_enable){
        bmi270_enable_sensor(ctx, 1, 1, 0, 0);
    }
    if (acc_enable && !gyr_enable){
        bmi270_enable_sensor(ctx, 1, 0, 0, 0);
    }

    uint8_t value;
    uint8_t pwr_ctrl_value;

    /* Configure power mode */
    switch (mode){
    case BMI270_PERFORMANCE_MODE:
        /* Disable advanced power save mode */
        value = 0x02;
        bmi270_reg_write(ctx, BMI270_REG_PWR_CONF, &value, 1);
        /* Configure acc and gyr to performance mode - (typ.) 970uA */
        bmi270_conf_pwr(ctx, &bmi270_performance_mode); 
        LOG_INF("BMI270 configured to performance mode");
        break;
    case BMI270_NORMAL_MODE:
        /* Disable advanced power save mode */
        value = 0x02;
        bmi270_reg_write(ctx, BMI270_REG_PWR_CONF, &value, 1); 
        /* Configure acc and gyr to normal mode - (typ.) 685uA */
        bmi270_conf_pwr(ctx, &bmi270_normal_mode); 
        LOG_INF("BMI270 configured to normal mode");
        break;
    case BMI270_LOW_POWER_MODE:
        /* Disable temperature and aux sensor */
        pwr_ctrl_value = 0x06; 
        bmi270_reg_write(ctx, BMI270_REG_PWR_CTRL, &pwr_ctrl_value, 1); 
        /* Enable advanced power save mode */
        value = 0x03;
        bmi270_reg_write(ctx, BMI270_REG_PWR_CONF, &value, 1); 
        /* Configure acc and gyr to low power mode - (typ.) 420uA */
        bmi270_conf_pwr(ctx, &bmi270_low_power_mode);
        LOG_INF("BMI270 configured to low power mode: gyro and acc enabled");
        break;
    case BMI270_SUSPEND_MODE:
        /* Check if all sensors are disabled */
        if (acc_enable || gyr_enable){
            LOG_ERR("Disable all sensors to enter suspend mode");
            break;
        }
        /* Enable advanced power save mode */
        value = 0x03;
        bmi270_reg_write(ctx, BMI270_REG_PWR_CONF, &value, 1); 
        /* Disable all sensors - (typ.) 3.5uA */
        bmi270_enable_sensor(ctx, 0, 0, 0, 0);
        LOG_INF("BMI270 configured to suspend mode: all sensors disabled"); 
        break;
    default:
        break;
    }
}

/**
 * @brief Read IMU values
 * 
 * Read raw values from IMU and convert them to physical values
 * 
 * @param ctx A pointer to a BMI270 sensor context
 * @param value An array to save the sensor values
 * @param acc_range Measurement range for acc
 * @param gyr_range Measurement range for gyr
 * 
 * @retval 0 if successful, err otherwise
 */
int bmi270_read_imu(BMI270_Context *ctx, BMI270_IMU_Value *value, uint8_t acc_range, uint8_t gyr_range){
    int ret = 0;
    uint8_t sensor_values[12];
    uint8_t acc_range_g; 
    uint16_t gyr_range_r;
    uint8_t factor_zx; 
    uint8_t factor_zx_page = 0; 

    switch (acc_range){
    case BMI270_ACC_RANGE_2G:
        acc_range_g = 2; 
        break;
    case BMI270_ACC_RANGE_4G:
        acc_range_g = 4;
        break;
    case BMI270_ACC_RANGE_8G:
        acc_range_g = 8;
        break;
    case BMI270_ACC_RANGE_16G:
        acc_range_g = 16;
        break;
    default:
        acc_range_g = 0; 
        break;
    }

    switch (gyr_range){
    case BMI270_GYR_RANGE_125:
        gyr_range_r = 125; 
        break;
    case BMI270_GYR_RANGE_250:
        gyr_range_r = 250;
        break;
    case BMI270_GYR_RANGE_500:
        gyr_range_r = 500;
        break;
    case BMI270_GYR_RANGE_1000:
        gyr_range_r = 1000;
        break;
    case BMI270_GYR_RANGE_2000:
        gyr_range_r = 2000;
        break;
    default:
        gyr_range_r = 0; 
        break;
    }

    /* Read sensor values */
    ret = bmi270_reg_read(ctx, BMI270_REG_ACC_X_LSB, sensor_values, sizeof(sensor_values)); 
    /* Read calibration factor on page 0 for gyro x - sign extended 7-bit */
    ret = bmi270_reg_write(ctx, BMI270_REG_FEAT_PAGE, &factor_zx_page, sizeof(factor_zx_page)); 
    ret = bmi270_reg_read(ctx, 0x3C, &factor_zx, 1); 
    if (factor_zx & BIT(6)){
        factor_zx |= 0x80;
    }else{
        factor_zx &= 0x7F; 
    }

    if (ret == 0){
        bmi270_acc_convert_raw(&value->accel.x, (int16_t)(sensor_values[0] | (sensor_values[1] << 8)), acc_range_g);
        bmi270_acc_convert_raw(&value->accel.y, (int16_t)(sensor_values[2] | (sensor_values[3] << 8)), acc_range_g);
        bmi270_acc_convert_raw(&value->accel.z, (int16_t)(sensor_values[4] | (sensor_values[5] << 8)), acc_range_g); 

        bmi270_gyr_convert_raw(&value->gyro.x, 
            ((int16_t)(sensor_values[6] | (sensor_values[7] << 8)) - (int8_t)factor_zx * ((int16_t)(sensor_values[10] | (sensor_values[11] << 8)))/512), 
            gyr_range_r);
        bmi270_gyr_convert_raw(&value->gyro.y, (int16_t)(sensor_values[8] | (sensor_values[9] << 8)), gyr_range_r);
        bmi270_gyr_convert_raw(&value->gyro.z, (int16_t)(sensor_values[10] | (sensor_values[11] << 8)), gyr_range_r); 
    }else{
        LOG_ERR("Failed to read sensor data");
    }

    return ret; 
}

int bmi270_conf_no_motion(BMI270_Context *ctx, bmi270_no_motion_config_t *config){
    int ret = 0;
    uint16_t duration, threshold;
    uint8_t nomo_param[4];
    uint8_t nomo_page = 2; 
    uint8_t default_page = 0; 

    // Duration is in 20ms steps
    duration = config->duration;
    if (duration > 0x1FFF) { // 13-bit value
        LOG_ERR("No-motion duration exceeds maximum");
        return -1; 
    }

    // The threshold value passed in the struct should be the raw register value.
    // The datasheet default is 0x90 for 70mg.
    threshold = config->threshold; 
    if (threshold > 0x7FF){ // 11-bit value
        LOG_ERR("No-motion threshold exceeds maximum");
        return -1; 
    }

    /* Change the page number to page 2 for no motion detection */
    ret = bmi270_reg_write(ctx, BMI270_REG_FEAT_PAGE, &nomo_page, sizeof(nomo_page)); 
    if (ret != 0) {
        LOG_ERR("Failed to switch to feature page 2");
        return ret;
    }
    k_usleep(BMI270_INTER_WRITE_DELAY_US); 

    /* Prepare the no motion detection parameters */
    nomo_param[0] = duration & 0xFF; 
    nomo_param[1] = ((duration >> 8) & 0x1F) | 
                    config->select_z << 7 | 
                    config->select_y << 6 | 
                    config->select_x << 5;
    nomo_param[2] = threshold & 0xFF;
    nomo_param[3] = ((threshold >> 8) & 0x07) | (0x06 << 3) | config->enable << 7; 

    ret = bmi270_reg_write(ctx, 0x30, nomo_param, sizeof(nomo_param)); 
    if (ret != 0){
        LOG_ERR("Failed to configure no-motion detection parameters");
    }

    k_usleep(BMI270_INTER_WRITE_DELAY_US);
    ret = bmi270_reg_write(ctx, BMI270_REG_FEAT_PAGE, &default_page, sizeof(default_page));
    if (ret != 0) {
        LOG_ERR("Failed to reset feature page to 0");
        return ret;
    }

    return 0; 
}

